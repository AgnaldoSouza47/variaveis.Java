public class tiposPrimitivos {
    public static void main (String[]args) {

            // Objetos são metodos e comportamentos ;exemplo: Byte 'B' maisculo
            //Tipos primitivos somente armazenam valores
            byte aByte = 5; // Exemplo byte armazena em abyte o valor 5
            short aShort = 5; // Exemplo short  armazena em aShort o valor 5
            int aInteger = 5; // Exemplo int  armazena em aInteger o valor 5
            long aLong = 10L;   // Exemplo long armazena em aLong o valor 10L
            float afloat = 10.2F;     // Exemplo float  armazena em afloat o valor 10,2F

            double aDouble = 10D;     // Exemplo double  armazena em aDouble o valor 10D
            boolean aBoolean=true;    // Exemplo armazena em aBoolean o valor verdadeiro ou falso
            char c = 'c';      // Exemplo caractere armazena em char o valor 'c'
           // Variaveis não estão sendo usadas
            //É apenas exemplo não mostrará nada quando roda

        }

    }




