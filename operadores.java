public class operadores {
    public static void main(String[] args) {
        System.out.println("Abaixo o resultado das operações");
        System.out.println("6 + 6 = " + (6 + 6));
        System.out.println("6 - 6 = " + (6 - 6));
        System.out.println("6 * 6 = " + (6 * 6));
        System.out.println("6 / 6 = " + (6 + 6));
        System.out.println("6 % 6 = " + (6 % 6));

        int numero = 5;
        float multiplica = 2.4f;
        numero = numero + 5;
        System.out.println(numero + multiplica);

    }
}
